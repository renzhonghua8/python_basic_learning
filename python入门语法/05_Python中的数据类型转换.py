# 将数字类型转换成字符串
number_str = str(11)
print(type(number_str),number_str)

float_str = str(13.14)
print(type(float_str),float_str)
# 将字符串转换成数字
# 整数
number_int = int("11")
print(type(number_int),number_int)
# 浮点数
number_float = float("13.14")
print(type(number_float),number_float)
# 错误示例，想要将字符串转换成数字，必须要求字符串内的内容都是数字
# number = int("黑马程序员")
# print(type(number),number)

# 整数转换成浮点数
number_f = float(11)
print(type(number_f),number_f)

# 浮点数转换成整数
number_z = int(13.14)
print(type(number_z),number_z)


